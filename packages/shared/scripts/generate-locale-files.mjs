/**
 * Ensures ml/hi/ta/kn locale files contain every key from en.ts.
 * Run: node packages/shared/scripts/generate-locale-files.mjs
 */
import { readFileSync, writeFileSync } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';
import {
  KEEP_ENGLISH_KEYS,
  hasRegionalScript,
  phraseTranslate,
} from './i18n-phrase-maps.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const i18nDir = join(__dirname, '../src/i18n');

function parseLocaleObject(filePath) {
  const text = readFileSync(filePath, 'utf8');
  const out = {};
  for (const m of text.matchAll(/^\s+(\w+):\s+'((?:\\'|[^'])*)',/gm)) {
    out[m[1]] = m[2].replace(/\\'/g, "'");
  }
  return out;
}

function parseEn() {
  const text = readFileSync(join(i18nDir, 'en.ts'), 'utf8');
  const out = {};
  for (const m of text.matchAll(/^\s+(\w+):\s+'((?:\\'|[^'])*)',/gm)) {
    out[m[1]] = m[2].replace(/\\'/g, "'");
  }
  return out;
}

function esc(s) {
  return s.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
}

function resolveValue(key, enValue, locale, existing, overrides) {
  if (KEEP_ENGLISH_KEYS.has(key)) return enValue;
  if (overrides[key]) return overrides[key];
  if (existing[key] && hasRegionalScript(existing[key], locale)) return existing[key];
  if (locale === 'ta' && taFull[key]) return taFull[key];
  if (locale === 'kn' && knFull[key]) return knFull[key];
  const translated = phraseTranslate(enValue, locale);
  if (translated !== enValue && hasRegionalScript(translated, locale)) return translated;
  if (existing[key]) return existing[key];
  return translated;
}

function writeLocale(locale, label, existing, en, overrides) {
  const lines = [
    `import type { I18nKey } from './en';`,
    '',
    `/** ${label} */`,
    `export const ${locale}: Partial<Record<I18nKey, string>> = {`,
  ];
  for (const key of Object.keys(en)) {
    const value = resolveValue(key, en[key], locale, existing, overrides);
    lines.push(`  ${key}: '${esc(value)}',`);
  }
  lines.push('};', '');
  writeFileSync(join(i18nDir, `${locale}.ts`), lines.join('\n'), 'utf8');
}

