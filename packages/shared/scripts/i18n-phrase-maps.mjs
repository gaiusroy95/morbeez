export function hasRegionalScript(text, locale) {
  if (!text) return false;
  if (locale === 'ml') return /[\u0D00-\u0D7F]/.test(text);
  if (locale === 'hi') return /[\u0900-\u097F]/.test(text);
  if (locale === 'ta') return /[\u0B80-\u0BFF]/.test(text);
  if (locale === 'kn') return /[\u0C80-\u0CFF]/.test(text);
  return false;
}

export function phraseTranslate(text, locale) {
  const map =
    locale === 'ml' ? ML_PHRASES :
    locale === 'hi' ? HI_PHRASES :
    locale === 'ta' ? TA_PHRASES :
    locale === 'kn' ? KN_PHRASES : [];
  let out = text;
  for (const [from, to] of map) {
    out = out.split(from).join(to);
  }
  return out;
}
