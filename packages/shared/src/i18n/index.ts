const strings: Record<AppLocale, Record<string, string>> = { en, hi, ml };

export function t(key: string, locale: AppLocale = 'en'): string {
  return strings[locale][key] ?? strings.en[key] ?? key;
}

export function formatDateInLocale(date: string | Date, locale: AppLocale = 'en'): string {
  const loc = locale === 'hi' ? 'hi-IN' : locale === 'ml' ? 'ml-IN' : 'en-IN';
  const d = typeof date === 'string' ? new Date(date) : date;
  return d.toLocaleDateString(loc, { day: 'numeric', month: 'short', year: 'numeric' });
}
